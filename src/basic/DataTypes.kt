package basic

// Data Types atau Tipe Data

fun main() {
    val first = "Kucing Item "
    val last = "Putih\n"
    val valueA = 10
    val valueB = 20
    print(first + last)
    print(valueA + valueB)
}

/*
output :

Kucing Item Putih
30
